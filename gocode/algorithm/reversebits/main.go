package main

import "fmt"

func main() {
	var res uint32
	res = (res << 1) | 1
	fmt.Printf("%b\n", res)
}

// input:  00000010 10010100 00011110 10011100
// output: 00111001 01111000 00101001 01000000
// 10011100 01001110 00100111 00010011 00001001 00000100 00000010 00000001
// -------0 ------00 -----000 ----0010 ---00110 --001110 -0011100 00111000
// -------0	------00 -----001 ----0011 ---00111 --001110 -0011100 00111001
func reverseBits(num uint32) uint32 {
	var res uint32
	for i := 0; i < 32; i++ {
		res = (res << 1) | (num & 1)
		num >>= 1
	}
	return res
}
